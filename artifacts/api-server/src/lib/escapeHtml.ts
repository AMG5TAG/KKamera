/** Escape user-controlled text before interpolating it into email/HTML so it
 *  can't inject markup. Pure & dependency-free (unit-tested). */
export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
